<template>
  <div class="container">
    <input v-model="userId" type="text" />
    <button @click="onLoadUser">load user</button>
    <nuxt-child />
  </div>
</template>

<script>
export default {
  data() {
    return {
      userId: ''
    }
  },
  methods: {
    onLoadUser() {
      this.$router.push('/users/' + this.userId)
    }
  }
}
</script>
